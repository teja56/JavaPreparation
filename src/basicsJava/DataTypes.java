package basicsJava;

public class DataTypes {

	public static void main(String[] args) {

		//primitive data types: int, float, double, char, boolean
		//it will not store in the form of object.
		
		//non -primitive data types: String
		//it will store in the form of object.
		
		// Data types:
		// 1. int: size : 4 bytes, range : -2147483648 to 2147483647
		// 1byte = 8 bits
		int i = 20;
		i = 20;
		i = 30;
		System.out.println(i);

		int j = -10;
		int k = 0;
		int age = 25;
		int total = 1000;

		// 2. byte: size : 1 byte, range : -128 to 127
		byte b1 = 10;
		byte age1 = 21;

		// 3 short : size: 2 bytes , range -32768 to 32767
		short s1 = 1299;

		// 4. long : size: 8 bytes, range:
		long l1 = 123413434;

		// 5. float: 4 bytes
		float f1 = (float) 12.33;
		float f2 = 12.45f;

		// 6. double: 8 bytes
		double d = 12.2324343;

		// 7. char: 2 bytes
		char c1 = 'a';
		char c2 = '#';
		char c3 = '1';

		// 8. boolean : not defined precisely but around 1 byte

		boolean b = true;
		boolean a = false;

		// 9. string : is not a datatype: it is class
		// its a collection of multiple characters
		String s = "abcd";
		String be = "hello world";
		String str = "1000";

	}

}
